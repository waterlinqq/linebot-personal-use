def _simplify(text: str) -> str:
    result = text
    for trad, simp in SIMPLIFIED_VARIANTS.items():
        result = result.replace(trad, simp)
    return result


def district_keywords(name: str, suffix: str) -> list[str]:
    keywords: list[str] = []
    for variant in (name, _simplify(name)):
        if variant and variant not in keywords:
            keywords.append(variant)
        if suffix:
            full = variant + suffix
            if full not in keywords:
                keywords.append(full)
            simp_suffix = _simplify(suffix)
            if simp_suffix != suffix:
                simp_full = variant + simp_suffix
                if simp_full not in keywords:
                    keywords.append(simp_full)
    return keywords


def build_catalog() -> dict:
    regions: dict = {}
    for region_key, aliases, districts in TAIWAN_REGIONS:
        district_map = {
            name: district_keywords(name, suffix)
            for name, suffix in districts
        }
        regions[region_key] = {
            "aliases": aliases,
            "districts": district_map,
        }
    return regions


def main() -> None:
    data = {
        "match_mode": "either",
        "min_price": 0,
        "max_price": 999999,
        "exclude_keywords": [],
        "reply_text": "接",
        "regions": build_catalog(),
    }
    OUTPUT.parent.mkdir(parents=True, exist_ok=True)
    with OUTPUT.open("w", encoding="utf-8") as f:
        yaml.dump(
            data,
            f,
            allow_unicode=True,
            default_flow_style=False,
            sort_keys=False,
            width=120,
        )
    print(f"Wrote {OUTPUT}")


if __name__ == "__main__":
    main()
